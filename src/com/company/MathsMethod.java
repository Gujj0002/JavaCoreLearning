package com.company;

public class MathsMethod {

    public static void main(String[] args) {

        System.out.println(Math.max(80, 75.25));
        System.out.println(Math.min(80,75));
        System.out.println(Math.abs(80.75));
        System.out.println(Math.pow(10,3));
        System.out.println(Math.addExact(2,5));
        System.out.println(Math.sqrt(10.25));
    }
}
